def countBalancingElements(arr):
    answer = 0

    for i in range(0, len(arr)):

        temp = arr.pop(i) # 요소 pop
        odd = arr[1::2] #인덱스가 홀수인 요소
        even = arr[0::2] #인덱스가 짝수인 요소
        if sum(odd) == sum(even):
            answer += 1
        arr.insert(i, temp)
    return answer

